let score; //variable for score
score = prompt("What's your score between 1-100?")

if (score >=90) {
    console.log("Grade A");
    
}else if (score >=80){
    console.log("Grade B");

}else if (score >=70){
    console.log("Grade C");

}else if (score <70){
    console.log("Grade D");

}else {
    console.log("Fail")
}


 